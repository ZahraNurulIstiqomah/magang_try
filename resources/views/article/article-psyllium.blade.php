<span class="article-question">Kandungan Nutrisi pada Psyllium Husk</span><br><br>
Psyllium adalah serat larut yang berasal dari biji Plantago ovata, yakni tanaman yang tumbuh di India. Melansir dari U.S. Department of Agriculture (USDA) | USAGo, , dalam 100 gram psyllium husk mengandung: <br>

> Karbohidrat: 88.9 gram. <br>
> Serat larut: 67 gram. <br>
> Serat tidak larut: 11 gram. <br>
> Besi: 16.7 gram. <br>
> Kalium: 967 mg. <br>
> Natrium: 111 mg.<br><br>

<span class="article-question">Manfaat Kesehatan Psyllium Husk</span><br>
Kamu bisa menggunakan psyllium sebagai suplemen makanan. Suplemen ini tersedia dalam bentuk sekam, butiran, kapsul, atau bubuk.
Psyllium dapat menyerap air dan menjadi senyawa kental yang bekerja dengan menghambat pencernaan di usus kecil. Lantas, apa kegunaan psyllium husk? Ini beberapa di antaranya: <br><br>

<span class="article-question">1. Meredakan sembelit</span><br>
Studi berjudul Fiber supplements and clinically proven health benefits: How to recognize and recommend an effective fiber therapy yang terbit pada Journal of the American Association of Nurse Practitioners mengatakan, psyllium adalah obat pencahar pembentuk tinja. 

Awalnya, psyllium bekerja dengan mengikat sebagian makanan yang dicerna dari lambung ke usus kecil. Kemudian, zat tersebut membantu penyerapan air, yang meningkatkan ukuran dan kelembapan tinja. Hal ini yang pada akhirnya membantu meredakan sembelit.<br><br>

<span class="article-question">2. Pengobatan diare</span><br>
Manfaat psyllium husk selanjutnya adalah meredakan diare. Dalam membantu mengatasi diare, zat tersebut bekerja dengan memadatkan tinja yang encer dan memperlambat gerakannya melalui usus besar.<br><br>

<span class="article-question">3. Manajemen gula darah</span><br>
Mengonsumsi suplemen psyllium dapat memengaruhi respons glikemik tubuh terhadap makanan. Hal ini yang pada akhirnya mampu menurunkan gula darah puasa dan kadar hemoglobin A1c.

Dalam sebuah penelitian, 51 orang pengidap diabetes tipe 2 dan sembelit mengonsumsi 10 gram psyllium dua kali sehari. Hal ini tak hanya menurunkan kadar gula darah, tetapi juga kolesterol.<br><br>

<span class="article-question">4. Meningkatkan rasa kenyang</span><br>
Psyllium mengandung serat yang membentuk senyawa kental. Nutrisi tersebut dapat membantu memperlambat pengosongan lambung dan meningkatkan rasa kenyang setelah makan.

Sebuah penelitian menemukan bahwa mengonsumsi 10.2 gram psyllium sebelum sarapan dan makan siang, menyebabkan penurunan rasa lapar keinginan makan, dan peningkatan rasa kenyang di antara waktu makan.<br><br>

<span class="article-question">5. Membantu menurunkan berat badan</span><br>
Dengan membantu meningkatkan dan mempertahankan rasa kenyang, psyllium hask bisa mengurangi asupan makanan secara keseluruhan. Hal ini dapat menjadi bagian dari program penurunan berat badan. Namun, khasiat ini masih memerlukan penelitian lebih lanjut.<br><br>

<span class="article-question">6. Menurunkan kolesterol</span><br>
erat larut seperti psyllium dapat mengikat kolesterol dalam proses pencernaan di usus. Zat tersebut meningkatkan pencernaan kolesterol sebelum sebelum melewati aliran darah. Hal tersebut yang pada akhirnya mampu menurunkan kadar kolesterol darah secara keseluruhan.<br><br>

<span class="article-question">7. Mempromosikan kesehatan jantung</span><br>
Semua jenis serat baik untuk mendukung kesehatan jantung. Cara kerjanya dengan meningkatkan kadar kolesterol baik dan menurunkan risiko penyakit jantung, stroke, diabetes tipe 2, serta obesitas.

Penelitian tahun 2020 terhadap 11 uji coba melaporkan bahwa psyllium dapat menurunkan tekanan darah sistolik sebesar 2.04 milimeter merkuri (mmHg). Hal ini yang berguna untuk membantu mengobati hipertensi.<br><br>

Itulah beberapa manfaat psyllium husk dalam mendukung kesehatan tubuh keseluruhan.  </p><br><br><br>
