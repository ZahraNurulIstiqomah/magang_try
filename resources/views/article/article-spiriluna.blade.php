        <p>&emsp;&emsp;&emsp;&emsp;<b>Halodoc, Jakarta</b> â€“ Spirulina termasuk ke dalam jenis ganggang dengan ciri fisik berwarna hijau kebiruan. Tanaman ini tumbuh secara alami pada perairan air tawar atau air laut yang kaya mineral.

Tanaman ini termasuk dalam kelompok superfood karena mengandung nutrisi yang lengkap, terutama kandungan protein yang tinggi. Kandungan inilah yang membuat spirulina baik bagi kesehatan tubuh.

Fungsinya mulai dari mengurangi gejala alergi, menjaga kesehatan jantung hingga menurunkan kadar kolesterol. Selain itu, jenis ganggang ini juga efektif membantu mengoptimalkan kesejahteraan mental. <br><br>

Manfaat Spirulina untuk Kesehatan
Spirulina mengandung protein, lemak, karbohidrat, dan berbagai vitamin serta mineral. Termasuk kalsium, kolin, folat, antioksidan, magnesium, kalium, fosfor, zat besi, dan vitamin C, K, A, serta B.

Lebih lengkap lagi, jenis ganggang ini juga mengandung zat yang bersifat antibakteri, antivirus, antiradang, dan antikanker.<br><br>

Faktanya, spirulina atau nama lainnya Arthospira menawarkan berbagai manfaat baik untuk tubuh. Ini termasuk:<br><br>

<span class="article-question">1. Spirulina mengurangi gejala alergi</span><br>
Spirulina dapat membantu mengurangi gejala alergi, seperti gatal dan bentol pada kulit, hidung berair atau tersumbat, serta bersin. Manfaat ini terjadi berkat kandungan zat antiradang pada tanaman herbal tersebut.

Meski begitu, efektivitasnya masih perlu pembuktian dengan studi ilmiah lanjutan. Pun, sebagian pengidap masalah kesehatan ini juga bisa saja memiliki alergi terhadap tanaman ini, sehingga tidak cocok mengonsumsinya.<br><br>

<span class="article-question">2. Menurunkan kolesterol dan menjaga kesehatan jantung</span><br>
Selain membantu mengurangi gejala alergi, tanaman herbal ini juga dapat membantu menurunkan kadar kolesterol jahat atau low density lipoprotein (LDL) dan trigliserida, sekaligus meningkatkan kadar kolesterol baik dalam tubuh.

Studi dalam Journal of the Science of Food and Agriculture menyebutkan, penggunaan 1 gram spirulina per hari menurunkan trigliserida sebesar 16.3 persen. Sementara kadar kolesterol jahat bisa menurun sebesar 10.1 persen.

Studi lainnya dalam BioMed Research International menyebutkan, oksidasi kolesterol menjadi salah satu pemicu sakit jantung. Namun, spirulina kaya akan sumber protein dan vitamin, terutama vitamin B12, mineral, karotenoid, dan phycocyanin. Beberapa nutrisi itu merupakan pengatur kolesterol dan kapasitas antioksidan dalam tubuh.<br>

Ini artinya, mengonsumsi spirulina secara rutin, termasuk dalam bentuk suplemen herbal, dapat membantu menjaga jantung tetap sehat.<br><br>

<span class="article-question">3. Meningkatkan imunitas tubuh</span><br>
Ada beberapa jenis antioksidan, mineral, dan vitamin yang terdapat pada ganggang ini. Misalnya zat besi, zinc, vitamin C, B6, dan E yang penting untuk meningkatkan imunitas tubuh. 

Tak hanya itu, ganggang ini juga mampu meningkatkan produksi dan kinerja sel darah putih, serta membantu proses pembentukan antibodi. Dengan begitu, kekebalan tubuh menjadi lebih kuat dalam melawan patogen penyebab penyakit.<br><br>

<span class="article-question">4. Membantu meringankan hipertensi</span><br>
Hipertensi menjadi penyebab dari banyak penyakit yang berbahaya, termasuk stroke, penyakit jantung, dan masalah ginjal. Faktanya, kandungan nutrisi dalam tanaman spirulina dapat membantu meningkatkan produksi senyawa nitrit oksida.

Senyawa ini bekerja dengan melebarkan pembuluh darah dan membuat tekanan darah lebih stabil. Studi dalam Nutrients menyebutkan, mengonsumsi 1 sampai 8 gram spirulina per hari secara signifikan mengurangi tekanan darah, terutama bagi orang dengan hipertensi yang parah.<br><br>

<span class="article-question">5. Menjaga kesehatan saluran cerna</span><br>
Spirulina mempunyai efek probiotik yang bisa meningkatkan jumlah dan kinerja dari bakteri baik dalam saluran pencernaan. Tak hanya itu, jenis ganggang ini juga dapat membantu meningkatkan kinerja sistem pencernaan sehingga semua nutrisi dari makanan bisa terserap dengan baik.<br><br>

<span class="article-question">6. Mengurangi risiko terserang kanker</span><br>
Kandungan tinggi antioksidan dalam tanaman ini mampu mencegah kerusakan pada sel tubuh. Selain itu, kandungan baik tersebut juga bermanfaat dalam mencegah dan mengurangi potensi serangan penyakit kanker. 

Salah satunya adalah kanker mulut. Faktanya, mengonsumsi suplemen dengan bahan dasar spirulina secara rutin dapat membantu mengelola fibrosis submukosa oral (OSMF), yaitu sejenis lesi prakanker pada mulut.

Meski begitu, perlu adanya dukungan penelitian lebih lanjut guna mendukung manfaat tanaman ini.<br><br>

<span class="article-question">7. Mengurangi gejala rhinitis alergi</span><br>
Rhinitis alergi adalah gangguan yang muncul dengan gejala berupa peradangan pada saluran hidung. Masalah ini terjadi karena alergen pada sekitar lingkungan, seperti serbuk sari, bulu hewan, atau debu.

Studi dalam ACTA Otorhinolaryngologica Italica menyebutkan, kandungan nutrisi pada spirulina efektif untuk mengurangi gejala masalah kesehatan tersebut. Tanaman herbal ini dapat bekerja lebih efektif daripada cetirizine, antihistamin untuk mengobati alergi dan mengurangi peradangan.<br><br>

<span class="article-question">8. Spirulina efektif melawan anemia</span><br>
Anemia adalah kondisi berupa penurunan kadar hemoglobin atau sel darah merah dalam darah. Ada banyak faktor yang menjadi penyebabnya, seperti malnutrisi, kelainan genetik, atau peradangan kronis.

Anemia menyebabkan perasaan lemah dan lelah berkepanjangan. Namun, dengan spirulina, gejala tersebut dapat teratasi. Sebab, kandungan dalam tanaman ini memicu peningkatan kadar hemoglobin dan fungsi kekebalan tubuh.<br><br>

<span class="article-question">9. Meningkatkan kekuatan dan daya tahan otot</span><br>
Kerusakan oksidatif akibat olahraga adalah penyebab utama penurunan kekuatan otot. Meski demikian, nutrisi yang terdapat dalam spirulina mampu bekerja aktif dengan mengurangi kerusakan tersebut. Caranya dengan meningkatkan penyerapan oksigen selama proses olahraga berlangsung.<br><br>

<span class="article-question">10. Spirulina efektif melawan anemia</span><br>
Faktanya, mengonsumsi suplemen spirulina dalam dosis mulai dari 0.8 sampai 8 gram setiap hari dapat menurunkan kadar gula darah puasa pada pengidap diabetes tipe 2.

Namun, tidak ada pengaruh yang signifikan terhadap kadar gula darah setelah makan atau kadar hemoglobin A1c guna mengendalikan gula darah jangka panjang. 

