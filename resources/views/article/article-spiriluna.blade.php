<span class="article-question">11. Meningkatkan kesejahteraan mental</span><br>
Tidak hanya melindungi tubuh dari berbagai masalah kesehatan fisik, spirulina juga dapat mendukung kesehatan mental. Bahkan, tanaman ini berperan penting dalam mengobati gangguan mood. Manfaat ini terjadi berkat tryptophan atau asam amino yang bekerja dengan meningkatkan jumlah serotonin pada otak.<br><br> 

Itu tadi beberapa manfaat tanaman spirulina untuk mendukung kesehatan tubuh. Meski bermanfaat, kamu harus memastikan bahwa tubuh tidak menunjukkan reaksi serius setelah mengonsumsi tanaman ini. Selain itu, beberapa manfaatnya juga masih perlu dukungan studi ilmiah sehingga kamu sebaiknya tidak melupakan penanganan medis. </p><br><br><br>
