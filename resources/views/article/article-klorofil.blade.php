        <p>&emsp;&emsp;&emsp;&emsp;<b>Halodoc, Jakarta</b> â€“ Klorofil adalah salah satu nutrisi yang terdapat pada warna hijau di daun. Kandungan ini dapat memberikan banyak manfaat untuk kesehatan tubuh saat  dikonsumsi.

Zat ini mampu menyerap menyerap energi dan mendapatkan nutrisi dari sinar matahari. Hal tersebut yang membuat klorofil mengandung vitamin, antioksidan, dan zat lainnya. Selain dengan mengonsumsi sayuran, kamu juga bisa mendapatkan zat ini melalui suplemen.<br><br>

<span class="article-question">Manfaat Daun Klorofil untuk Kesehatan</span><br><br>

<span class="article-question">1. Mencegah Penuaan Dini</span><br>
Mengutip jurnal Dovepress, mengoleskan gel yang mengandung klorofil ke kulit mampu mengurangi tanda-tanda penuaan, terutama yang disebabkan paparan matahari. Zat ini dapat lebih efektif jika dikombinasikan dengan tretinoin. Dengan begitu, tanda-tanda penuaan bisa saja tidak terlihat lagi.<br><br>

<span class="article-question">2. Perawatan Jerawat</span><br>
Diketahui jika klorofil mampu membantu tubuh dalam mengatasi masalah jerawat. Mengutip J Drugs Dermatol, penggunaan gel yang mengandung klorofil mampu membantu dalam pengurangan jerawat di wajah, serta mengecilkan pori-pori yang ada.

Manfaat untuk jerawat bisa lebih efektif lagi saat klorofil topikal digunakan bersamaan dengan fototerapi. Kombinasi ini dapat membuat lesi jerawat lebih sedikit, masalah jerawat berkurang, serta minyak pada kulit juga berkurang.<br><br>

<span class="article-question">3. Meningkatkan Produksi Sel Darah Merah</span><br>
Klorofil secara kimiawi mirip dengan hemoglobin, protein pada sel darah merah yang membawa oksigen ke seluruh tubuh. Maka dari itu, konsumsi zat ini dapat membantu tubuh dalam mengatasi masalah produksi sel darah merah. Gangguan yang bisa teratasi adalah defisiensi hemoglobin, seperti anemia dan thalasemia.<br><br>

<span class="article-question">4. Penyembuhan Luka</span><br>
Klorofil sudah diteliti sejak lama jika ampuh dalam membantu luka agar cepat sembuh. Bahkan, ada penelitian yang menyebut jika klorofil mampu membantu dalam penyembuhan luka bedah, serta mencegah infeksi. Bahkan, beberapa ahli medis meresepkan obat yang mengandung klorofil, agar luka lebih cepat sembuh.<br><br>

<span class="article-question">5. Mencegah Kanker</span><br>
Mengutip jurnal dari HHS Public Access, kandungan klorofil mampu mengurangi risiko terjadinya tumor hati dan tumor perut. Bahkan, mengonsumsi klorofil secara rutin secara signifikan mampu mengurangi ukuran tumor.

Pada penelitian lainnya, klorofil dapat membatasi zat aflatoksin, senyawa yang menyebabkan kanker, saat masuk ke tubuh. Maka dari itu, seseorang yang terserang kanker direkomendasikan untuk makan sayuran berdaun hijau, seperti bayam.<br><br>

<span class="article-question">6. Penurunan Berat Badan</span><br>
Beberapa sumber menyebut jika klorofil dalam bentuk cair mampu membantu penurunan berat badan. Disebutkan, seseorang yang rutin mengonsumsi suplemen dengan kandungan klorofil, penurunan berat badannya dapat terjadi secara signifikan, dibandingkan yang tidak. 

Bahkan, kandungan ini juga dapat mengurangi kadar kolesterol berbahaya. Sebab, kolesterol berlebih di dalam tubuh dapat meningkatkan risiko dari banyak penyakit, termasuk penyakit jantung.<br><br>

Itulah beberapa daun klorofil terhadap kesehatan, terutama saat dikonsumsi secara rutin.  Untuk mendapatkan manfaat yang disebutkan, pastikan untuk mengonsumsi klorofil secara rutin, baik dalam bentuk suplemen, cairan, hingga sayuran.</p><br><br><br>
